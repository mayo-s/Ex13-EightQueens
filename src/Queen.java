
public class Queen {

	private int xCord;
	private int yCord;
	
	public Queen(int xCord, int yCord){
		this.xCord = xCord;
		this.yCord = yCord;
		
	}
	
	public void setxCord(int xCord){
		this.xCord = xCord;
	}
	
	public void setyCord(int yCord){
		this.xCord = yCord;
	}
	
	public int getyCord(){
		return yCord;
	}
	
	public int getxCord(){
		return xCord;
	}
}
